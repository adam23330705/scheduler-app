/** 从本地存储恢复 */
function 恢复朋友圈() {
  try {
    const 存储 = localStorage.getItem('moments_data');
    if (存储) {
      const 数据 = JSON.parse(存储);
      朋友圈动态 = 数据.动态 || [];
      朋友圈点赞 = 数据.点赞 || {};
      朋友圈评论 = 数据.评论 || {};
    }
  } catch {}

  if (朋友圈动态.length === 0) {
    生成初始动态();
  }
}

/** 保存到本地 */
function 保存朋友圈() {
  try {
    localStorage.setItem('moments_data', JSON.stringify({
      动态: 朋友圈动态.slice(-100),
      点赞: 朋友圈点赞,
      评论: 朋友圈评论,
    }));
  } catch {}
}

function 生成动态ID() {
  return 'm_' + Date.now() + '_' + Math.floor(Math.random() * 1000);
}

