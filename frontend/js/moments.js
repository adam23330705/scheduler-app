/** 格式化时间 */
function 格式化动态时间(时间字符串) {
  const 现在 = new Date();
  const 时间 = new Date(时间字符串);
  const 差 = 现在 - 时间;

  if (差 < 60000) return '刚刚';
  if (差 < 3600000) return Math.floor(差 / 60000) + '分钟前';
  if (差 < 86400000) return Math.floor(差 / 3600000) + '小时前';
  if (差 < 172800000) return '昨天';
  return `${时间.getMonth() + 1}月${时间.getDate()}日`;
}

/** 点赞动态 */
function 点赞动态(动态id) {
  if (!朋友圈点赞[动态id]) 朋友圈点赞[动态id] = [];
  const 用户 = 应用状态?.用户?.username || '我';

  const 索引 = 朋友圈点赞[动态id].indexOf(用户);
  if (索引 >= 0) {
    朋友圈点赞[动态id].splice(索引, 1);
  } else {
    朋友圈点赞[动态id].push(用户);
  }
  保存朋友圈();
  渲染朋友圈();
}

/** 评论动态 */
function 评论动态(动态id, 内容) {
  if (!内容.trim()) return;
  if (!朋友圈评论[动态id]) 朋友圈评论[动态id] = [];
  const 用户 = 应用状态?.用户?.username || '我';

  朋友圈评论[动态id].push({
    用户名: 用户,
    内容: 内容.trim(),
    时间: new Date().toISOString(),
  });

  // 角色自动回复评论
  const 动态 = 朋友圈动态.find(d => d.id === 动态id);
  if (动态 && 角色数据[动态.角色] && Math.random() < 0.6) {
    setTimeout(() => {
      const 评论池 = ['谢谢！', '收到~', '嗯嗯', '哈哈', '👍', '你说得对！', '是这样的'];
      朋友圈评论[动态id].push({
        用户名: 动态.角色,
        内容: 评论池[Math.floor(Math.random() * 评论池.length)],
        时间: new Date().toISOString(),
      });
      保存朋友圈();
      渲染朋友圈();
    }, 1000 + Math.random() * 2000);
  }

  保存朋友圈();
  渲染朋友圈();
}

/** 渲染朋友圈 */
function 渲染朋友圈() {
  const 容器 = document.getElementById('朋友圈列表');
  if (!容器) return;

  const 用户 = 应用状态?.用户?.username || '我';

  let html = '';
  朋友圈动态.forEach(动态 => {
    // 判断是公司成员还是竞争对手
    const 是对手 = 动态.是竞争对手;
    let 角色;
    if (角色数据[动态.角色]) {
      角色 = 角色数据[动态.角色];
    } else if (竞争对手人设[动态.角色]) {
      const 人设 = 竞争对手人设[动态.角色];
      角色 = {
        名字: 动态.角色,
        头像emoji: 人设.emoji,
        颜色: '#6b7280',
        职位: 人设.职位,
      };
    } else {
      return;
    }

    const 点赞列表 = 朋友圈点赞[动态.id] || [];
    const 是否已赞 = 点赞列表.includes(用户);
    const 评论列表 = 朋友圈评论[动态.id] || [];

    html += `
      <div class="朋友圈动态项 ${是对手 ? '竞争对手动态' : ''}">
        <div class="朋友圈头部">
          <div class="朋友圈头像" style="background:${角色.颜色}" ${!是对手 ? `onclick="打开角色对话('${动态.角色}')"` : ''}>${角色.头像emoji || '👤'}</div>
          <div class="朋友圈用户信息">
            <span class="朋友圈用户名" style="color:${角色.颜色}">${动态.角色}</span>
            <span class="朋友圈职位">${角色.职位}${是对手 ? ' · 竞品' : ''}</span>
          </div>
          <span class="朋友圈时间">${格式化动态时间(动态.时间)}</span>
        </div>
        <div class="朋友圈内容">${动态.内容}</div>
        ${动态.类型 ? `<span class="朋友圈类型标签 ${是对手 ? '同行标签' : ''}">${动态.类型}</span>` : ''}
        ${!是对手 ? `
        <div class="朋友圈操作">
          <button class="朋友圈操作按钮 ${是否已赞 ? '已赞' : ''}" onclick="点赞动态('${动态.id}')">
            ${是否已赞 ? '❤️' : '🤍'} ${点赞列表.length || ''}
          </button>
          <button class="朋友圈操作按钮" onclick="显示评论框('${动态.id}')">
            💬 ${评论列表.length || ''}
          </button>
        </div>
        ` : ''}
        ${评论列表.length > 0 ? `
          <div class="朋友圈评论区">
            ${评论列表.map(评 => `
              <div class="朋友圈评论">
                <span class="评论用户" ${角色数据[评.用户名] ? `onclick="打开角色对话('${评.用户名}')"` : ''}>${评.用户名}</span>
                <span class="评论内容">${评.内容}</span>
              </div>
            `).join('')}
          </div>
        ` : ''}
        ${!是对手 ? `
        <div class="朋友圈评论输入" id="评论框-${动态.id}" style="display:none">
          <input type="text" placeholder="写评论..." onkeydown="if(event.key==='Enter'){评论动态('${动态.id}',this.value);this.value=''}">
        </div>
        ` : ''}
      </div>
    `;
  });

  容器.innerHTML = html || '<div class="空状态"><div class="空状态-图标">🌐</div><p>暂无动态</p></div>';
}

/** 显示评论输入框 */
function 显示评论框(动态id) {
  const 框 = document.getElementById(`评论框-${动态id}`);
  if (框) {
    框.style.display = 框.style.display === 'none' ? 'flex' : 'none';
    if (框.style.display === 'flex') 框.querySelector('input').focus();
  }
}

