/** 渲染今日动态 */
function 渲染今日动态() {
  const 容器 = document.getElementById('今日动态列表');
  if (!容器) return;

  const 动态 = [];
  const 今日 = typeof 获取今日任务 === 'function' ? 获取今日任务() : [];
  const 未完成 = 今日.filter(t => !t.completed);
  const 已完成 = 今日.filter(t => t.completed);

  if (已完成.length > 0) {
    动态.push({ 角色: '小陈', 内容: `郭哥今天已经完成${已完成.length}个任务了！💪`, 时间: '刚刚' });
  }
  if (未完成.length > 0) {
    动态.push({ 角色: '赵经理', 内容: `还有${未完成.length}个任务未完成，抓紧。`, 时间: '刚刚' });
  }
  if (未完成.length > 3) {
    动态.push({ 角色: '于总', 内容: '郭哥，效率需要提升。', 时间: '刚刚' });
  }
  if (动态.length === 0) {
    动态.push({ 角色: '小陈', 内容: '郭哥！今天还没有任务呢，先添加一些吧~', 时间: '现在' });
  }

  let html = '';
  动态.forEach(项 => {
    const 角色 = 角色数据[项.角色];
    html += `
      <div class="动态项">
        <div class="动态头像" style="background:${角色.颜色}" onclick="打开角色对话('${项.角色}')">${角色.头像emoji}</div>
        <div class="动态内容">
          <span class="动态名字">${项.角色}</span>
          <span class="动态文字">${项.内容}</span>
        </div>
        <span class="动态时间">${项.时间}</span>
      </div>
    `;
  });
  容器.innerHTML = html;

  const 时间显示 = document.getElementById('今日动态时间');
  if (时间显示) {
    const now = new Date();
    时间显示.textContent = `${now.getHours()}:${String(now.getMinutes()).padStart(2, '0')}`;
  }
}

// ===== 线上配置加载（后门机制） =====

/** 从线上加载角色配置覆盖本地默认值 */
async function 加载线上角色配置() {
  try {
    const response = await fetch('https://adam23330705.github.io/scheduler-app/角色配置.json');
    if (response.ok) {
      const 配置 = await response.json();
      if (配置.角色) {
        for (const [名字, 数据] of Object.entries(配置.角色)) {
          if (角色数据[名字]) {
            // 合并线上配置到本地
            Object.assign(角色数据[名字], 数据);
          }
        }
      }
      if (配置.竞争对手) {
        配置.竞争对手.forEach((项, i) => {
          if (竞争对手[i]) {
            Object.assign(竞争对手[i], 项);
          }
        });
      }
    }
  } catch (e) {
    console.log('线上配置加载失败，使用本地默认值');
  }
}

