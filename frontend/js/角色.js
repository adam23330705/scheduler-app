/** 根据关键词匹配获取回复分类 */
function 匹配回复分类(用户输入) {
  const 输入 = 用户输入.toLowerCase();
  for (const 规则 of 关键词规则) {
    for (const 词 of 规则.关键词) {
      if (输入.includes(词)) return 规则.分类;
    }
  }
  return '闲聊'; // 默认闲聊
}

/** 生成本地规则引擎回复 */
function 生成本地回复(角色名, 用户输入) {
  const 角色 = 角色数据[角色名];
  if (!角色) return '...';

  const 分类 = 匹配回复分类(用户输入);
  let 模板池 = 对话模板[角色名]?.[分类];

  // 心情覆盖
  if (角色.心情 === '不爽' && 对话模板[角色名]?.心情_不爽) {
    if (Math.random() < 0.4) 模板池 = 对话模板[角色名].心情_不爽;
  }
  if (角色.心情 === '开心' && 对话模板[角色名]?.心情_开心) {
    if (Math.random() < 0.4) 模板池 = 对话模板[角色名].心情_开心;
  }

  if (!模板池 || 模板池.length === 0) 模板池 = 对话模板[角色名]?.闲聊 || ['...'];
  return 模板池[Math.floor(Math.random() * 模板池.length)];
}

/** 基于任务状态生成主动提醒 */
function 生成主动提醒(角色名) {
  const 今日 = 获取今日任务();
  const 未完成 = 今日.filter(t => !t.completed);
  const 已完成 = 今日.filter(t => t.completed);
  const 角色 = 角色数据[角色名];

  // 小陈：提醒最多
  if (角色名 === '小陈') {
    if (未完成.length > 0) {
      const 随机任务 = 未完成[Math.floor(Math.random() * 未完成.length)];
      return `郭哥！你还有${未完成.length}个任务没做呢！"${随机任务.title}"别忘了哦~`;
    }
    if (已完成.length > 0 && 未完成.length === 0) {
      return '郭哥好厉害！今天的任务都做完了！🎉';
    }
    const 小陈提醒 = 对话模板.小陈.提醒;
    return 小陈提醒[Math.floor(Math.random() * 小陈提醒.length)];
  }

  // 赵经理：催KPI
  if (角色名 === '赵经理') {
    if (未完成.length > 3) {
      return `郭哥，你今天还有${未完成.length}个任务没完成，效率得提一下。`;
    }
    const 催更 = 对话模板.赵经理.催更;
    return 催更[Math.floor(Math.random() * 催更.length)];
  }

  // 于总：抽查
  if (角色名 === '于总') {
    if (未完成.length > 已完成.length) {
      return '郭哥，你今天完成的比没完成的还少？跟我说说怎么回事。';
    }
    return null; // 于总不是时刻说话的
  }

  // 小周：催素材
  if (角色名 === '小周') {
    const 催更 = 对话模板.小周.催更;
    return 催更[Math.floor(Math.random() * 催更.length)];
  }

  return null;
}

/** 更新角色心情 */
function 更新角色心情() {
  const 今日 = 获取今日任务();
  const 未完成 = 今日.filter(t => !t.completed);
  const 完成率 = 今日.length > 0 ? 今日.filter(t => t.completed).length / 今日.length : 0.5;

  // 于总：完成率低于30%就不爽
  if (完成率 < 0.3) 角色数据.于总.心情 = '不爽';
  else if (完成率 > 0.8) 角色数据.于总.心情 = '开心';
  else 角色数据.于总.心情 = '正常';

  // 赵经理同理
  if (完成率 < 0.3) 角色数据.赵经理.心情 = '不爽';
  else if (完成率 > 0.8) 角色数据.赵经理.心情 = '开心';
  else 角色数据.赵经理.心情 = '正常';

  // 小陈：完成率高就开心
  if (完成率 > 0.6) 角色数据.小陈.心情 = '开心';
  else if (完成率 < 0.2) 角色数据.小陈.心情 = '不爽';
  else 角色数据.小陈.心情 = '正常';

  // 小周：基本正常，偶尔不爽
  角色数据.小周.心情 = 完成率 < 0.2 ? '不爽' : '正常';
  角色数据.李老师.心情 = '正常';
}

