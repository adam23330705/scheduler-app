/** 调用DeepSeek API获取角色回复 */
async function 获取AI回复(角色名, 用户消息, 对话上下文) {
  const 角色 = 角色数据[角色名];
  if (!角色) return '...';

  // 构建消息列表
  const messages = [
    { role: 'system', content: 角色.systemPrompt },
  ];

  // 加入任务上下文（让AI知道当前状态）
  const 今日 = typeof 获取今日任务 === 'function' ? 获取今日任务() : [];
  const 未完成 = 今日.filter(t => !t.completed);
  const 已完成 = 今日.filter(t => t.completed);

  let 上下文信息 = `当前时间：${new Date().toLocaleString('zh-CN')}\n`;
  上下文信息 += `今日任务：共${今日.length}个，已完成${已完成.length}个，未完成${未完成.length}个\n`;
  if (未完成.length > 0) {
    上下文信息 += `未完成任务：${未完成.map(t => t.title).join('、')}\n`;
  }

  messages.push({ role: 'system', content: `当前状态信息：\n${上下文信息}` });

  // 加入历史对话（最近10条）
  if (对话上下文 && 对话上下文.length > 0) {
    const 最近 = 对话上下文.slice(-10);
    最近.forEach(msg => {
      messages.push({
        role: msg.角色 === '我' ? 'user' : 'assistant',
        content: msg.内容,
      });
    });
  }

  // 加入当前用户消息
  messages.push({ role: 'user', content: 用户消息 });

  try {
    const response = await fetch(DEEPSEEK_API_URL, {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${DEEPSEEK_API_KEY}`,
      },
      body: JSON.stringify({
        model: 'deepseek-chat',
        messages: messages,
        max_tokens: 200,
        temperature: 0.8,
      }),
    });

    if (!response.ok) {
      throw new Error(`API错误: ${response.status}`);
    }

    const data = await response.json();
    return data.choices?.[0]?.message?.content?.trim() || '...';
  } catch (error) {
    console.error('DeepSeek API调用失败:', error);
    // 降级到本地回复
    return 生成本地回复(角色名, 用户消息);
  }
}

